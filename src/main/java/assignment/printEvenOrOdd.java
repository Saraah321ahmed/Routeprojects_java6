package assignment;

public class printEvenOrOdd {
    public void evenoddnum() {
        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 0)
                System.out.println("num " + i + " is even");
            else
                System.out.println("num " + i + " is odd");

        }
    }
}
